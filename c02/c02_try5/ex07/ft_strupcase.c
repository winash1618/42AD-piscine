char	*ft_strupcase(char	*str)
{
	int	i;

	i = 0;
	while (*str)
	{
		if (*str >= 'a' && *str <= 'z')
		{
			*str = *str - 32;
		}
		str++;
		i++;
	}
	str -= i;
	return (str);
}
