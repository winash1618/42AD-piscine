int	ft_str_is_numeric(char	*str)
{
	int	i;
	int	k;

	i = 0;
	k = 0;
	while (str[i] != '\0')
	{
		if (!(str[i] >= '0' && str[i] <= '9'))
		{
			k++;
		}	
		i++;
	}
	if (i == 0 || k > 0)
	{
		return (1);
	}
	else
	{
		return (0);
	}
}
