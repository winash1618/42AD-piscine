int	ft_str_is_alpha(char *str)
{
	int	i;
	int	k;

	i = 0;
	k = 0;
	while (str[i] != '\0')
	{
		if (!(str[i] >= 'A' && str[i] <= 'Z'))
		{
			if (!(str[i] >= 'a' && str[i] <= 'z'))
			{
				k++;
			}
		}
		i++;
	}
	if (i == 0 || k > 0)
	{
		return (1);
	}
	else
	{
		return (0);
	}
}
