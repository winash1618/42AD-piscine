#include <unistd.h>

void	ft_putstr(char *str)
{
	char c;
	int i;
	
	i = 0;
	while (str[i] != '\0')
	{
		c = str[i];
		write(1, &c, 1);
		i++;
	}
}

int main(void)
{
	char *str;

	str = "toto";
	ft_putstr(str);
	return (0);
}
